    private void butInviaMessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butInviaMessActionPerformed
        if(Condivisi.Instance().getConnessioneAttule()!=null)
        {
            Pacchetto p=new Pacchetto("m",Txtmessaggio.getText());
            p.address=Condivisi.Instance().getConnessioneAttule();
            Condivisi.Instance().MettiPacchettoOut(p);
            TxtIp.setText("");
        }
    }//GEN-LAST:event_butInviaMessActionPerformed

    private void DisconnettiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisconnettiActionPerformed
        if(Condivisi.Instance().getConnessioneAttule()!=null)
        {
            Pacchetto p=new Pacchetto("d","");
            p.address=Condivisi.Instance().getConnessioneAttule();
            Condivisi.Instance().MettiPacchettoOut(p);
            Condivisi.Instance().SettaStatoConnessione(StatoConnessione.DisConnesso);
        }
    }//GEN-LAST:event_DisconnettiActionPerformed

    private void ButConnettiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButConnettiActionPerformed
        Pacchetto p=new Pacchetto("c",Condivisi.Instance().mioNome);
        try {
            p.address=InetAddress.getByName(TxtIp.getText());
            Condivisi.Instance().MettiPacchettoOut(p);
            Condivisi.Instance().SettaStatoConnessione(StatoConnessione.InAttessa );
            Annulla.setVisible(true);
            TxtStatiConnessione.setText("in connessione con "+TxtIp.getText());
            TxtIp.setText("");
        } catch (UnknownHostException ex) {
            Logger.getLogger(Interfaccia.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ButConnettiActionPerformed

    private void AnnullaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AnnullaActionPerformed
        Condivisi.Instance().SettaStatoConnessione(StatoConnessione.DisConnesso);
        Annulla.setVisible(false);
        TxtStatiConnessione.setText("");
    }//GEN-LAST:event_AnnullaActionPerformed

